void wait_for_game_start(void) {
    for (volatile int i = 0; i < 500000; i++);

    uint32_t initial_counts[8];
    for (int i = 0; i < 8; i++) {
        initial_counts[i] = daos_btn_get_count_by_index(i);
    }

    uint8_t key_pressed = 0;
    while (!key_pressed) {
        daos_btn_update_all();
        for (int i = 0; i < 8; i++) {
            if (daos_btn_get_count_by_index(i) != initial_counts[i]) {
                key_pressed = 1;
                break;
            }
        }
        for (volatile int i = 0; i < 50000; i++);
    }

    daos_uart_puts("[GAME] Key pressed - Starting!\r\n");
}

