void button_update_task(void) {
    static uint32_t last_d14 = 0;
    static uint8_t d14_initialized = 0;
    static uint32_t last_d15 = 0;

    daos_btn_update_all();

    uint32_t current_d14 = daos_btn_get_count_by_index(8);
    uint32_t current_d15 = daos_btn_get_count_by_index(9);

    if (!d14_initialized) {
        last_d14 = current_d14;
        last_d15 = current_d15;
        d14_initialized = 1;
        daos_uart_puts("[D14/D15] Button tasks initialized and synced\r\n");
        daos_sleep_ms(50);
        return;
    }

    // En main.c, dentro de button_update_task():

    if (current_d14 != last_d14) {
        last_d14 = current_d14;

        daos_uart_puts("\r\n╔═══════════════════════════════════╗\r\n");
        daos_uart_puts("║   D14 PRESSED - RETURNING...      ║\r\n");
        daos_uart_puts("╚═══════════════════════════════════╝\r\n");

        if (shell_mode_active || binario_actual != NULL) {
            daos_cleanup_current_game();
        }

        daos_gfx_clear(DAOS_COLOR_BLACK);
        daos_gfx_draw_text_large(50, 100, "RETURNING", DAOS_COLOR_WHITE, DAOS_COLOR_BLACK, 2);
        daos_gfx_draw_text_large(65, 130, "TO MENU", DAOS_COLOR_YELLOW, DAOS_COLOR_BLACK, 2);

        for (volatile int i = 0; i < 2000000; i++);

        // Matar TODAS las tareas y regresar al menú
        extern void sched_kill_all_tasks(void);
        sched_kill_all_tasks();

        d14_initialized = 0;
        return;
    }

    if (!shell_mode_active && current_d15 != last_d15) {
        last_d15 = current_d15;

        daos_uart_puts("\r\n[RESET] D15 Pressed - Restarting game...\r\n");

        daos_gfx_clear(DAOS_COLOR_BLACK);
        daos_gfx_draw_text_large(50, 110, "RESTARTING", DAOS_COLOR_WHITE, DAOS_COLOR_BLACK, 2);

        for (volatile int i = 0; i < 300000; i++);

        daos_cleanup_current_game();

        extern void sched_kill_all_tasks(void);
        sched_kill_all_tasks();

        daos_gfx_clear(DAOS_COLOR_BLACK);

        if (binario_actual != NULL) {
            if (binario_actual->init != NULL) {
                binario_actual->init();
            }

            daos_task_create(binario_actual->input_task, DAOS_PRIO_NORMAL);
            daos_task_create(binario_actual->logic_task, DAOS_PRIO_NORMAL);
            daos_task_create(binario_actual->render_task, DAOS_PRIO_NORMAL);

            daos_task_create(button_update_task, DAOS_PRIO_CRITICAL);
            daos_task_create(system_monitor, DAOS_PRIO_LOW);

            extern void sched_start(void);
            sched_start();
        }

        d14_initialized = 0;
    }

    daos_sleep_ms(50);
}

